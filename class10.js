// mathematical operation shorthand
var price = 35;
// price = price + 35;
price += 35;
console.log(price)

var cost = 10;
// cost = cost - 5;
cost -= 5;
console.log(cost)


var orangePrice = 30;
// orangePrice = orangePrice * 3;
orangePrice *= 3;
console.log(orangePrice)

var bananaPrice = 30;
// bananaPrice = bananaPrice / 2;
bananaPrice /= 2;
console.log(bananaPrice)
